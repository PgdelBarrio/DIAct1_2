    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
         
         
        try{
            
            String name;
            String surname;
            String mail;
            String age;
            int edad=0;
            String phone;

            name=txtNombre.getText();
            surname=txtApellidos.getText();
            mail=txtApellidos.getText();
            age=txtEdad.getText();
            edad=Integer.parseInt(age);
            phone=txtTelefono.getText();
            
            int limite=5;
            if(listaAlumnos.size()<limite)
            {
                alumno.setNombre(name);
                alumno.setApellidos(surname);
                alumno.setEmail(mail);
                alumno.setEdad(edad);
                alumno.setTelefono(phone);

                listaAlumnos.add(alumno);
                
                JOptionPane.showMessageDialog(null, "Alumno registrado correctamente", "REGISTRO", JOptionPane.INFORMATION_MESSAGE);
            }
            else
            {
                java.awt.EventQueue.invokeLater(new Runnable() {
                    public void run() {
                        new frmError().setVisible(true);
                    }
                });
            }
            
            txtNombre.setText(null);
            txtApellidos.setText(null);
            txtEmail.setText(null);
            txtEdad.setText(null);
            txtTelefono.setText(null);
            
            if(alumno.getEdad()<=edmenor)
            {
                edmenor=alumno.getEdad();
                this.lblAlumnoMenor.setText(alumno.getNombre()+" "+alumno.getApellidos());
            }
            
            System.out.println(alumno);
            
         }
        catch (NumberFormatException e){
            JOptionPane.showMessageDialog(null, "La edad debe ser numerica", "Error", JOptionPane.ERROR_MESSAGE);
        }
        
        
    }//GEN-LAST:event_btnAddActionPerformed

